
public class Arithmetic {

    public static void main(String[] args) {

        int a = 13;
        int b = 5;

        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);

        // Modulus operator to get reminder in integer division
        System.out.println(a % b);

        double x = 12.0;
        double y = 5.0;

        System.out.println(x + y);
        System.out.println(x - y);
        System.out.println(x * y);
        System.out.println(x / y);
    }
}
